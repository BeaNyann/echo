package com.github.beanyann

/**
 * Echoes the input text.
 *
 * This function is a simple identity function that returns the input text.
 *
 * ## Example
 * ```
 * val text = "Hello, world!"
 * val echoed = id(text)
 * println(echoed) // Hello, world!
 * ```
 *
 * @param text the text to echo
 * @return the input text
 */
fun id(text: String) = text